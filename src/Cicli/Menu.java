
package Cicli;


public class Menu {
    private int lato;
    private int numeroLati;

    public Menu() {
    }

    public Menu(int lato, int numeroLati) {
        this.lato = lato;
        this.numeroLati = numeroLati;
    }

    public int getLato() {
        return lato;
    }

    public void setLato(int lato) {
        this.lato = lato;
    }

    public int getNumeroLati() {
        return numeroLati;
    }

    public void setNumeroLati(int numeroLati) {
        this.numeroLati = numeroLati;
    }
}
    
    
